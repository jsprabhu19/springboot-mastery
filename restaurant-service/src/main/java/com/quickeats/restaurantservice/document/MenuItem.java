package com.quickeats.restaurantservice.document;

import java.math.BigDecimal;

/**
 * Nested document representing a menu item offered by a restaurant.
 */
public class MenuItem {

    private String id;
    private String name;
    private String description;
    private BigDecimal price;
    private boolean available;

    public MenuItem() {}

    public MenuItem(String id, String name, String description, BigDecimal price, boolean available) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.available = available;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public boolean isAvailable() {
        return available;
    }

    public void setAvailable(boolean available) {
        this.available = available;
    }
}
